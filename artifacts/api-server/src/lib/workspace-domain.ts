/**
 * Multi-workspace support: same app serves different corpora (industrial pumps, Gabriel dataviz workshops, generic).
 * Drives system-prompt composition for the visualization model.
 */

export type WorkspaceDomain = "grundfos" | "gabriel" | "generic";

const RULE1_ANCHOR = "━━━ RULE 1: FOLLOW THE SERVER DIRECTIVE — DO NOT OVERRIDE IT ━━━";

export function normalizeWorkspaceDomain(
  input: string | null | undefined
): WorkspaceDomain {
  const s = (input ?? "grundfos").toLowerCase().trim();
  if (s === "gabriel") return "gabriel";
  if (s === "generic" || s === "neutral" || s === "other") return "generic";
  return "grundfos";
}

export function workspaceFooterLabel(domain: WorkspaceDomain): string {
  switch (domain) {
    case "gabriel":
      return "Generated by Meeting AI Visualizer · Gabriel";
    case "generic":
      return "Generated by Meeting AI Visualizer";
    default:
      return "Generated by Meeting AI Visualizer · Grundfos";
  }
}

export function brandNameForDomain(domain: WorkspaceDomain): string {
  switch (domain) {
    case "gabriel":
      return "Gabriel";
    case "generic":
      return "the organization";
    default:
      return "Grundfos";
  }
}

