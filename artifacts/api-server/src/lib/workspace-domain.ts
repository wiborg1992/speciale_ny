/**
 * Adapt the full Grundfos-centric system prompt for Gabriel or generic workspaces.
 */
export function adaptSystemPromptForDomain(
  grundfosPrompt: string,
  domain: WorkspaceDomain
): string {
  if (domain === "grundfos") return grundfosPrompt;

  const i = grundfosPrompt.indexOf(RULE1_ANCHOR);
  if (i < 0) return grundfosPrompt;

  const preamble = domain === "gabriel" ? GABRIEL_PREAMBLE : GENERIC_PREAMBLE;
  let out = `${preamble}\n\n${grundfosPrompt.slice(i)}`;

  const brandTech = domain === "gabriel" ? GABRIEL_BRAND_TECH : GENERIC_BRAND_TECH;
  out = out.replace(
    /━━━ GRUNDFOS BRAND IDENTITET ━━━[\s\S]*?(?=━━━ HMI \/ SCADA INTERFACE)/,
    `${brandTech}\n\n`
  );

  out = out.replace(
    /━━━ HMI \/ SCADA INTERFACE — GRUNDFOS iSOLUTIONS DESIGN LANGUAGE ━━━/,
    "━━━ HMI / SCADA INTERFACE — NEUTRAL INDUSTRIAL DASHBOARD ━━━"
  );

  out = out.replace(
    /When active, generate an interface indistinguishable from Grundfos iSolutions Suite — all details below\. Everything must look like real production software\./,
    "When active, generate a convincing operations / control-room dashboard that fits the transcript's industry. Do not use Grundfos trademarks, the iSolutions name, or pump-specific chrome unless the transcript explicitly discusses Grundfos or those products. Everything must look like real production software."
  );

  out = out.replace(
    /Grundfos X-logo øverst \(SVG,/,
    "Workspace mark or simple lettermark øverst (SVG,"
  );

  out = out.replace(
    /- Footer: diskret "Generated by Meeting AI Visualizer · Grundfos"/,
    `- Footer: diskret "${workspaceFooterLabel(domain)}"`
  );

  out = out.replace(
    /• Color: Grundfos navy \+ blue, subtle red accents for risk items\n  • Footer: Grundfos logo strip in navy/,
    "• Color: match transcript context — analytic neutrals plus one strong accent\n  • Footer: minimal label strip consistent with workspace (no unrelated corporate logo unless stated in transcript)"
  );

  return out;
}

export function adaptAuxiliarySystemPrompt(
  template: string,
  domain: WorkspaceDomain
): string {
  if (domain === "grundfos") return template;
  let t = template.replace(
    /Match the same industrial \/ Grundfos HMI tone[\s\S]*?dark: #111827 panels,/,
    "Match the transcript industry — for dark dashboards use panels around #111827, "
  );
  t = t.replace(
    /Footer: "Generated by Meeting AI Visualizer · Grundfos"/,
    `Footer: "${workspaceFooterLabel(domain)}"`
  );
  t = t.replace(
    /Design: light background \(#f8fafc\), navy \(\#002A5C\) header strip, blue \(\#0077C8\) accents\./,
    "Design: light professional background (#f8fafc), restrained header strip, single accent colour derived from context (default deep blue–green if unspecified)."
  );
  return t;
}
